// 数字类型-数字
// 内容：
// 1. num 关键字修饰变量，可整数或小数
// 2. int 关键字修饰变量，仅整数
// 3. double 关键字修饰变量，浮点数（支持赋值整数）

void main(){
    // 1. num 关键字修饰变量，可整数或小数
    num count = 10;
    print(count);
    // 2. int 关键字修饰变量，仅整数
    int num2 = 10;
    print(num2);
    // 3. double 关键字修饰变量，浮点数（支持赋值整数）
    double price =100;
    print(price);
}
